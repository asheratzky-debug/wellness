export function getTodayCard(): DailyCard {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((now.getTime() - start.getTime()) / 86_400_000);
  return CARDS[dayOfYear % CARDS.length];
}
